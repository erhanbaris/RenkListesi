    private static func initCombineColor()
    {
        colorListCombine = [String, UIColor]();
        
        for item in ColorContainer.colorListTurkish{
            var liste = colorListCombine.filter{$0.0 == item.0 || $0.1.rgbHex == item.1.rgbHex};
            
            if (liste.count == 0)
            {
                colorListCombine.append(item)
            }
        }
        
        for item in ColorContainer.colorListEnglish{
            
            var liste = colorListCombine.filter{$0.0 == item.0 || $0.1.rgbHex == item.1.rgbHex};
            
            if (liste.count == 0)

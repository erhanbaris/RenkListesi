            }
        }
    }
    
    static func getCurrentColors() -> [(String, UIColor)]
    {
        if (AppConfig.GetColorPalette() == ColorPalette.English)
        {
            return ColorContainer.colorListEnglish;
        }
        else if (AppConfig.GetColorPalette() == ColorPalette.Combine)
        {
            return ColorContainer.colorListCombine;
        }
        else
        {
            return ColorContainer.colorListTurkish;
        }
    }
    
